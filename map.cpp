Map::Map() {
	dirt = TextureManager::LoadTexture("assets/darktile.png");
	grass = TextureManager::LoadTexture("assets/lighttile.png");
	water = TextureManager::LoadTexture("assets/almostwhite.png");

	LoadMap(lvl1);

	src.x = 0;
	src.y = 0;
	src.w = dest.w = 16;
	src.h = dest.h = 16;

	dest.x = dest.y = 0;
}

Map::~Map() {
	SDL_DestroyTexture(grass);
	SDL_DestroyTexture(water);
	SDL_DestroyTexture(dirt);
}

void Map::LoadMap(int arr[32][128]) {
	for (int row = 0; row < 32; row++) {

		for (int column = 0; column < 128; column++) {

			map[row][column] = arr[row][column];
		}
	}
}

void Map::DrawMap() {
	int type = 0;
	for (int row = 0; row < 32; row++) {

		for (int column = 0; column < 128; column++) {

			type = map[row][column];

			//sets size of each tile
			dest.x = column * 16;
			dest.y = row * 16;

			switch (type) {
			case 0:
				TextureManager::Draw(dirt, src, dest);
				break;
			case 1:
				TextureManager::Draw(grass, src, dest);
				break;
			case 2:
				TextureManager::Draw(water, src, dest);
				break;
			default:
				break;
			}
		}
	}
}